print(f"a) 1, 3, 5, 7, 9")
print("Essa é uma sequência de números ímpares consecutivos\n")

print(f"b) 2, 4, 8, 16, 32, 64, 128")
print(
    "Essa sequência é uma progressão geométrica de razão 2 (cada número é o dobro do anterior)\n"
)

print(f"c) 0, 1, 4, 9, 16, 25, 36, 49")
print("Essa é a sequência dos quadrados perfeitos: 0², 1², 2², 3², 4², 5², 6²...\n")

print(f"d) 4, 16, 36, 64, 100")
print("Essa sequência são os quadrados de números pares: 2², 4², 6², 8²...\n")

print(f"e) 1, 1, 2, 3, 5, 8, 13")
print(
    "Essa é a sequência de Fibonacci, onde cada número é a soma dos dois anteriores\n"
)

print(f"f) 2, 10, 12, 16, 17, 18, 19, 20")
print(
    "Essa é a lógica de alternar entre somar 8 e somar 1. Exemplo: 2 + 8 = 10, 10 + 2 = 12, 12 + 4 = 16, 16 + 1 = 17, 17 + 1 = 18, 18 + 1 = 19...\n"
)
